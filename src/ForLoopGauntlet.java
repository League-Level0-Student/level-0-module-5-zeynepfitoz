
public class ForLoopGauntlet {
public static void main(String[] args) {
	for (int i = 0; i < 101; i++) {
		System.out.println(i);
	}
	for (int w = 101; w > 0; w--) {
		System.out.println(w);
	}
	for (int i = 2; i < 101; i+=2) {
		System.out.println(i);
	}
	for (int i = 1; i < 101; i+=2) {
		System.out.println(i);
	}
}
}
